#include "Mesh.hpp"

namespace omi {

//------------------------------------------------------------------------------
//                                  CONSTRUCTOR
//------------------------------------------------------------------------------

Mesh::Mesh(const std::string& id) :
    Renderable(id) {
}

//------------------------------------------------------------------------------
//                                   DESTRUCTOR
//------------------------------------------------------------------------------

Mesh::~Mesh() {
}

//------------------------------------------------------------------------------
//                           PROTECTED MEMBER FUNCTIONS
//------------------------------------------------------------------------------

void Mesh::render() {

    std::cout << "rendering mesh" << std::endl;

    glBindTexture(GL_TEXTURE_2D, 0);
    glColor4f(1.0f, 0.0f, 0.0f, 1.0f);
    glBegin(GL_QUADS);
    glVertex3f( 1.0f,  1.0f, 0.0);
    glVertex3f(-1.0f,  1.0f, 0.0);
    glVertex3f(-1.0f, -1.0f, 0.0);
    glVertex3f( 1.0f, -1.0f, 0.0);
    glEnd();
}

} // namespace omi